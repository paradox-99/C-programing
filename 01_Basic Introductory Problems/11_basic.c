#include<stdio.h>

int main()
{
    unsigned int a=545435547;
    unsigned long int b= 1054841987;
    unsigned long long int c= 98715987654321921;
    unsigned short int d=64352;

    printf("The unsigned int value: %u\n",a);
    printf("The unsigned long int value: %lu\n",b);
    printf("The unsigned long long int value: %llu\n",c);
    printf("The unsigned short int value: %hu",d);
    return 0;
}